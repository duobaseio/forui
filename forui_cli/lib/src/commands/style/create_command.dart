/// A theme can be generated using the Forui CLI:
/// ```shell
/// dart forui theme create [theme name]
/// ```
///
/// See:
/// * https://forui.dev/docs/guides/customizing-themes for customizing themes.
/// * https://forui.dev/docs/guides/customizing-widget-styles for customizing individual widget styles.''';

/// Maps every style's lowercased name and aliases to its [Style], for resolving the user's arguments.
final registry = {
  for (final MapEntry(:key, :value) in Style.values.asNameMap().entries) ...{
    key.toLowerCase(): value,
    for (final alias in value.aliases) alias.toLowerCase(): value,
  },
};

class StyleCreateCommand extends ForuiCommand {
  static String _fold(String value) => value.toLowerCase().replaceAll(RegExp('[-_ ]'), '');

  static String _snake(String pascal) =>
      pascal.replaceAllMapped(RegExp('([a-z0-9])([A-Z])'), (m) => '${m[1]}_${m[2]}').toLowerCase();

  static String _camel(String pascal) => pascal[0].toLowerCase() + pascal.substring(1);

  @override
  final name = 'create';

  @override
  final aliases = ['c'];

  @override
  final description = 'Create Forui widget style files.';

  @override
  final arguments = '[styles]';

  final Configuration configuration;

  StyleCreateCommand(this.configuration) {
    argParser
      ..addFlag('all', abbr: 'a', help: 'Generate all styles.', negatable: false)
      ..addFlag('force', abbr: 'f', help: 'Overwrite existing files if they exist.', negatable: false)
      ..addOption(
        'output',
        abbr: 'o',
        help: 'The output directory or file, relative to the project directory.',
        defaultsTo: configuration.style,
      );
  }

  @override
  void run() {
    final output = argResults!['output'] as String;
    final all = argResults!.flag('all');
    final interactive = terminal.interactive && !globalResults!.flag('no-input');
    final rest = argResults!.rest;

    if (rest.isNotEmpty && all) {
      terminal.error('Cannot use "[styles]" and "--all" at the same time.\n');
      exit(1);
    }

    // `autocompleteMultiselect` returns the initial (empty) selection when not interactive, so a script would silently
    // create nothing. Error instead.
    if (rest.isEmpty && !all && !interactive) {
      terminal.error('No style specified. Run "forui style ls" to see all styles.\n');
      exit(1);
    }

    // Validate explicit arguments up-front, before opening any interactive frame.
    if (rest.isNotEmpty && !_validate(rest)) {
      exit(1);
    }

    if (!interactive) {
      _generate(all ? Style.values.asNameMap().keys.toList() : rest, output: output, interactive: false);
      return;
    }

    intro('Create widget styles');

    final List<String> styles;
    if (all) {
      styles = Style.values.asNameMap().keys.toList();
    } else if (rest.isNotEmpty) {
      styles = rest;
    } else {
      styles = switch (autocompleteMultiselect<String>(
        message: 'Styles',
        options: {
          '': [
            for (final style in Style.values.toList()..sort((a, b) => a.aliases.first.compareTo(b.aliases.first)))
              SelectOption(style.aliases.first, hint: '(${style.type})'),
          ],
        },
        // Match the type shown in the hint too, and fold `-`, `_`, and spaces together so "autocomplete field",
        // "autocomplete-field", and "autocompletefield" all match.
        filter: (option, query) {
          final q = _fold(query);
          // Every option above is built with a non-null hint.
          return _fold(option.label).contains(q) || _fold(option.hint!).contains(q);
        },
        min: 1,
        maxItems: 25,
      )) {
        Value(:final value) => value,
        Cancelled() => _cancelled('No styles created.'),
      };
    }

    final count = _generate(styles, output: output, interactive: true);
    outro('Created $count file(s).');
  }

  bool _validate(List<String> styles) {
    var valid = true;
    for (final style in styles) {
      if (registry.containsKey(style.toLowerCase())) {
        continue;
      }

      valid = false;

      final suggestions =
          registry.keys.map((e) => (e, e.startsWith(style) ? 1 : distance(style, e))).where((e) => e.$2 <= 3).toList()
            ..sort((a, b) => a.$2.compareTo(b.$2));

      final buffer = StringBuffer('Could not find a style named "$style".');
      if (suggestions.isNotEmpty) {
        buffer.write('\nDid you mean one of these?');
        for (final (suggestion, _) in suggestions) {
          buffer.write('\n  $suggestion');
        }
      }

      terminal.error('$buffer\n');
    }

    if (!valid) {
      terminal.error('Run "forui style ls" to see all styles.\n');
    }

    return valid;
  }

  /// Generates the style files for [styles] and returns the number of files written. Multiple styles collapse into one
  /// namespaced file when [output] is a `.dart` file.
  int _generate(List<String> styles, {required String output, required bool interactive}) {
    final force = argResults!.flag('force');

    final paths = <String, List<String>>{};
    final existing = <String>{};

    for (final style in styles) {
      final file = _snake(registry[style.toLowerCase()]!.type.substring(1));
      final path =
          '${configuration.root.path}${Platform.pathSeparator}'
          '${output.endsWith('.dart') ? output : '$output${Platform.pathSeparator}$file.dart'}';

      (paths[path] ??= []).add(style);
      if (File(path).existsSync()) {
        existing.add(path);
      }
    }

    if (!force && existing.isNotEmpty) {
      final overwrite =
          interactive &&
          switch (confirm(message: 'Overwrite ${existing.length} existing file(s)?', initialValue: false)) {
            Value(:final value) => value,
            Cancelled() => false,
          };

      if (!overwrite) {
        if (interactive) {
          _cancelled('No styles created.');
        }

        terminal.error('${existing.length} file(s) already exist; pass --force to overwrite.\n');
        exit(1);
      }
    }

    // Writes a style's root function plus its privatised nested closures. When [many], wraps them in a
    // `Custom… on Never` extension of `static` members so several styles can share one file without name clashes.
    void reduce(StringBuffer buffer, List<String> closure, {required bool many}) {
      final root = registry[closure.first.toLowerCase()]!;

      if (many) {
        buffer
          ..writeln('extension Custom${root.type} on Never {')
          ..write('static ');
      }
      buffer.writeln(root.source);

      for (final nested in closure.skip(1)) {
        if (many) {
          buffer.write('static ');
        }
        final style = registry[nested.toLowerCase()]!;
        final function = _camel(style.type.substring(1));
        final index = style.source.indexOf(function);
        buffer.write('${style.source.substring(0, index)}_${style.source.substring(index)}\n');
      }

      if (many) {
        buffer.writeln('}');
      }
    }

    for (final MapEntry(key: path, value: group) in paths.entries) {
      final buffer = StringBuffer();

      if (registry[group.first.toLowerCase()] case Style(:final closure) when group.length == 1) {
        buffer.writeln(_unnamespaced);
        reduce(buffer, closure, many: false);
      } else {
        buffer.writeln(_namespaced);
        for (final style in group) {
          reduce(buffer, registry[style.toLowerCase()]!.closure, many: true);
        }
      }

      File(path)
        ..createSync(recursive: true)
        ..writeAsStringSync(formatter.format(buffer.toString()));

      if (interactive) {
        success('Created ${Uri.file(path)}');
      } else {
        terminal.write('Created ${Uri.file(path)}\n');
      }
    }

    return paths.length;
  }

  Never _cancelled(String message) {
    cancel(message);
    exit(130);
  }
}
